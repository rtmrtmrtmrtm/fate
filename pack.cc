// strip white space, turn lower to upper case.
std::string
fixcall(std::string call)
{
  while(call.size() > 0 && isspace(call[0]))
    call.erase(0);
  while(call.size() > 0 && isspace(call[call.size()-1]))
    call.erase(call.size()-1);
  for(int i = 0; i < (int) call.size(); i++){
    if(islower(call[i]))
      call[i] = toupper(call[i]);
  }
  return call;
}

//
// given a text call-sign,
// return a 28-bit integer.
// high bits correspond to first call-sign character.
// returns false if it can't pack the call.
//
bool
pack_call_28(std::string call, unsigned int &x)
{
  std::string orig_call = call;
  
  call = fixcall(call);
  if(call.size() > 2 && call.size() < 6 && !isdigit(call[2])){
    call = " " + call;
  }
  while(call.size() < 6)
    call = call + " ";
  
  std::string c1 = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ ";
  std::string c2 = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";
  std::string c3 = "0123456789";
  std::string c4 = "ABCDEFGHIJKLMNOPQRSTUVWXYZ ";

  // c1 c2 c3 c4 c4 c4

  fprintf(stderr, "pack_call_28 >>>%s<<< >>>%s<<<\n", orig_call.c_str(), call.c_str());

  assert(c1.find(call[0]) != std::string::npos);
  assert(c2.find(call[1]) != std::string::npos);
  assert(c3.find(call[2]) != std::string::npos);
  assert(c4.find(call[3]) != std::string::npos);
  assert(c4.find(call[4]) != std::string::npos);
  assert(c4.find(call[5]) != std::string::npos);

  x = 0;

  x += c1.find(call[0]);

  x *= c2.size();
  x += c2.find(call[1]);

  x *= c3.size();
  x += c3.find(call[2]);

  x *= c4.size();
  x += c4.find(call[3]);

  x *= c4.size();
  x += c4.find(call[4]);

  x *= c4.size();
  x += c4.find(call[5]);

  return true;
}

