//
// turn e.g. FN42 into a 15-bit number.
//
unsigned int
pack_grid(std::string grid)
{
  if(grid.size() < 4)
    return 0x7ffff;
  
  int x1 = grid[0] - 'A'; // A .. R
  int x2 = grid[1] - 'A'; // A .. R
  int x3 = grid[2] - '0'; // 0 .. 9
  int x4 = grid[3] - '0'; // 0 .. 9

  int lon = 179 - (x1*10 + x3);
  int lat = x2*10 + x4;
  int x = (lon * 180) + lat;

  return x;
}

std::vector<double>
pack_cq(std::string call, std::string grid, int rate, double hz)
{
  int a87[87];
  memset(a87, 0, sizeof(a87));

  // starts with 000

  // 50 bits of call sign
  unsigned long long x = pack_50(call);
  setbits(a87, 3, 50, x);

  int g = pack_grid(grid);
  g |= 0x8000; // CQ (rather than heartbeat)
  
  setbits(a87, 53, 16, g);

  setbits(a87, 69, 3, 0); // CQ CQ CQ

  setbits(a87, 72, 3, 3); // itype=first+last

  return pack_any(a87, rate, hz);
}

//
// generate a directed message.
// cmd is an index into unpack.cc's directed_cmds[],
//   e.g. 19 is HW CPY and 25 is SNR.
// itype is 1 for start, 2 for finish, 3 for both.
//
std::vector<double>
pack_directed(std::string my_call, std::string other_call,
              int cmd,
              int extra,
              int itype,
              int rate, double hz)
{
  int a87[87];
  memset(a87, 0, sizeof(a87));

  // directed, so starts with 011
  a87[0] = 0;
  a87[1] = 1;
  a87[2] = 1;

  unsigned int x;
  pack_call_28(my_call, x);
  setbits(a87, 3, 28, x);

  pack_call_28(other_call, x);
  setbits(a87, 3+28, 28, x);

  setbits(a87, 3+28+28, 5, cmd);

  if(extra < 0) extra = 0;
  if(extra > 63) extra = 63;
  setbits(a87, 66, 6, extra);

  setbits(a87, 72, 3, itype);

  return pack_any(a87, rate, hz);
}
